package ex_04_AssignmentOperators;

public class Lab33_divideandassignoperator {
    public static void main(String[] args){
        // Divide and Assign (/=)
        // Equivalent to: number = number / 4;
        int number=200;
        number /= 4;
        System.out.println("After using '/=' (240 / 4): " + number);
    }
}
