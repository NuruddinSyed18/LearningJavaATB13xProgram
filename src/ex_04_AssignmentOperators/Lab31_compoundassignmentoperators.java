package ex_04_AssignmentOperators;

public class Lab31_compoundassignmentoperators {
    public static void main(String[] args){
        // --- 2. Compound Assignment Operators ---
        // These operators perform an operation and then assign the result back to the variable.

        // Add and Assign (+=)
        // Equivalent to: number = number + 20;
        int number = 100;
        number += 20;
        System.out.println("After using '+=' (100 + 20): " + number);
    }
}
