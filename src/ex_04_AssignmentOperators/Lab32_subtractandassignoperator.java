package ex_04_AssignmentOperators;

public class Lab32_subtractandassignoperator {
    public static void main(String [] args){
        // Subtract and Assign (-=)
        // Equivalent to: number = number - 40;
        int number=100;
        number -= 40;
        System.out.println("After using '-=' (120 - 40): " + number);
    }
}
